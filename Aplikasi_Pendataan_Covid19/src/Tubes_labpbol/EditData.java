    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String nama = txtNama.getText(); //input nama
        String jk = ""; // input jk dari radio button
        if (rbtnLk.isSelected()) { //jika rbtnlk dipilih
            jk = rbtnLk.getText(); //maka rbtnlk yang diinput
        }
        else { //jika rbtnpr dipilih
            jk = rbtnPr.getText(); //maka rbtnpr yang dipilih
        }
        String umur = txtUmur.getText(); // input umur
        String profesi = txtProfesi.getText(); //input profesi
        String status = (String)cbStatus.getSelectedItem(); // input data dari dropdown
        String alamat = txtaAlamat.getText(); // input alamat
        String sql;
        
        if(id_pasien != null){ //jika id_pasien tidak null, maka query update ke tabel data_pasien
        sql = "UPDATE data_pasien SET nama='"+nama+"',jenis_kelamin='"+jk+"',umur='"+umur+"',profesi='"+profesi+"',alamat='"+alamat+"',status='"+status+"' WHERE id = '"+id_pasien+"'";
        }else{
        sql = "INSERT INTO data_pasien (nama, jenis_kelamin, umur, profesi, alamat, status) VALUES ('"+nama+"','"+jk+"','"+umur+"','"+profesi+"','"+alamat+"','"+status+"');";
        } // query input ke tabel data_pasien
        try
        {
            PreparedStatement statement = connection.prepareStatement(sql); //deklarasi objek preparedstatement untuk mempersiapkan pemanggilan sql sebelum dieksekusi
            statement.executeUpdate(); //sql dieksekusi
            JOptionPane.showMessageDialog(this, "Berhasil " + x);
            statement.close();
            dispose(); // menutup frame
            new Covid19().setVisible(true); //membuka frame covid19
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

