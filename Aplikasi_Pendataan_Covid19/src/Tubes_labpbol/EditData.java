    public EditData() {
        initComponents();
        connection = koneksi.connection();
        
        if(id_pasien!=null){  // jika id_pasien tidak null
        x="Edit Data"; // maka edit data
        }else{
        x="Tambah Data";  //maka tambah data
        }
        btnEdit.setText(x);
        jLabel1.setText(x);
        if (id_pasien != null){ //jika id_pasien tidak null
        try 
        {
            String sql = "SELECT nama, jenis_kelamin, umur, profesi, alamat, status FROM data_pasien WHERE id = '"+id_pasien+"'"; //query untuk mengambil data pasien berdasarlan id_pasien yang dipilih
            Statement st = connection.createStatement(); //deklarasi object statement  untuk  pemanggilan sql sebelum dieksekusi
            ResultSet rs = st.executeQuery(sql);  // mengeksekusi query sql
            while(rs.next()){
                txtNama.setText(rs.getString(1)); //pengambilan nama dari data pasien yang dipilih
               
                if (rs.getString(2).equals("Laki-laki")) { //pengambilan jk pada data pasien jika dia laki-laki
                      rbtnLk.setSelected(true); //maka pilih rbtnlk
                        } else { 
                      rbtnPr.setSelected(true); //maka pilih rbtnpr
                    }
                
                txtUmur.setText(rs.getString(3)); //pengambilan umur dari data pasien yang dipilih
                txtProfesi.setText(rs.getString(4)); // pengambilan profesi dari data pasien yang dipilih
                txtaAlamat.setText(rs.getString(5)); //pengambilan alamat dari data pasien yang dipilih
                
                cbStatus.setSelectedItem(rs.getString(6)); //pengambilan data status dari data pasien yang dipilih
        }
    rs.close();
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
}

